package com.example.recyclerviewdemo;

public class Person {
    private int mImage;
    private String mTen;

    public Person(int mImage, String mTen) {
        this.mImage = mImage;
        this.mTen = mTen;
    }

    public int getmImage() {
        return mImage;
    }

    public void setmImage(int mImage) {
        this.mImage = mImage;
    }

    public String getmTen() {
        return mTen;
    }

    public void setmTen(String mTen) {
        this.mTen = mTen;
    }


}
